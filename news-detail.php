                    <h6>Binh Duong Province – Vietnam – 27th November, 2019 </h6>
                    <p>
                        Hanh Phuc International Hospital and its affiliated clinics announced on 27 Nov 2019 that it is the first maternity (mother and child) hospital in Vietnam to be accredited with the Joint Commission International’s Gold Seal of Approval®, through its continuous upholding of internationally recognised standards. The Gold Seal of Approval® is a symbol of the highest quality that reflects an organisation’s commitment to providing safe and effective patient care and increased financial efficiency by reducing risk.
                    </p>
                    <p>
                        The JCI Gold Seal of Approval® accreditation ceremony was held solemnly in the presence of Ms. Paula Wilson - President and Chief Executive Officer of Joint Commission Resources (JCR) and its international division, Joint Commission International (JCI); PhD. Nguyen Duc Vinh – Director, General of Department of Maternal Health and Children; PhD. Nguyen Huy Quang – Director, General of Department of Legislation – Ministry of Health, and representatives of Binh Duong Department of Health, as well as other distinguished guests from Ho Chi Minh City Department of Health and partners, the Chief Executives of the Clermont Group, the Protrade Corporation, Hoan My Medical corporation, Hoan My hospitals, the JCI accredited organisations in Vietnam, other privates hospitals in Ho Chi Minh City and Can Tho province, and, over 100+ media partners and the press.
                    </p>
                    <p>
                        The JCI standards comprised over twelve hundred measurable elements (1,200+); two hundred and ninety-one (291) standards distributed in fourteen chapters (14) of which eight (08) chapters are focused on patient-centred standards, and six (06) chapters on healthcare organisation management standards. The core values revolve around ensuring patient and staff safety, and the overarching aim is to secure ‘excellence in care’ in the organisation. The JCI has unparalleled global scale, setting international standards for 20+ years, and is the recognised world leader in healthcare accreditation with the highest number of programs (10) approved and endorsed by ISQua; and over one thousand (1000+) JCI accredited organisations in more than hundred (100+) countries around the world.
                    </p>
                    <p>
                        Our JCI journey at Hanh Phuc Hospital was officially launched in February 2017: the hospital team including the Board of Directors, managers, doctors, nurses, clinical and non-clinical staff and contractors went through a vigorous preparation process with more than 400+ discussions sessions, 200+ training programs, 45+ quality improvement projects on patient safety and risk management (including 50+ practice drills on fire safety), emergency codes and emergency preparedness. The whole team monitored the JCI 1200+ MEs in their daily practice, documented records, and strictly followed policies and protocol. An online incident reporting system, HM-115, was introduced to enhance reporting of adverse events and incidents, as a measure to promote a ‘Zero Harm’ culture throughout the hospital.
                    </p>
