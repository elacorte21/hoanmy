                    <p>HANH PHUC IS THE FIRST MOTHER AND CHILD HOSPITAL IN VIETNAM TO BE ACCREDITED WITH JOINT COMMISSION INTERNATIONAL ‘GOLD SEAL OF APPROVAL’</p>
                    <p>
                        Hanh Phuc International Hospital is recognised as the first Mother and Child Hospital in Vietnam to obtain the Joint Commission International ‘Gold Seal of Approval’ after an arduous 2-year journey. The hospital achieved a score of 99% of the 1,200+ “measurable elements”, and is proud to take its place on the global JCI map.
